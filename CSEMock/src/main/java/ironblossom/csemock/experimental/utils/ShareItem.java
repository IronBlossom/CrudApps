package ironblossom.csemock.experimental.utils;

public class ShareItem {
    private String shareName;
    private String companyName;
    private float totalPoint;
    private float index1;
    private float index2;
    private float alertHi;
    private float alertLo;
    private float dayHi;
    private float dayLo;

    public String getShareName() {
        return shareName;
    }

    public void setShareName(String shareName) {
        this.shareName = shareName;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public float getTotalPoint() {
        return totalPoint;
    }

    public void setTotalPoint(float totalPoint) {
        this.totalPoint = totalPoint;
    }

    public float getIndex1() {
        return index1;
    }

    public void setIndex1(float index1) {
        this.index1 = index1;
    }

    public float getIndex2() {
        return index2;
    }

    public void setIndex2(float index2) {
        this.index2 = index2;
    }

    public float getAlertHi() {
        return alertHi;
    }

    public void setAlertHi(float alertHi) {
        this.alertHi = alertHi;
    }

    public float getAlertLo() {
        return alertLo;
    }

    public void setAlertLo(float alertLo) {
        this.alertLo = alertLo;
    }

    public float getDayHi() {
        return dayHi;
    }

    public void setDayHi(float dayHi) {
        this.dayHi = dayHi;
    }

    public float getDayLo() {
        return dayLo;
    }

    public void setDayLo(float dayLo) {
        this.dayLo = dayLo;
    }
}
